import { mkdir, writeFile } from 'node:fs/promises'
import path from 'node:path'
import config from '#constants'
import runCommand from '#utils/tools/runCommand.ts'

type ScaffoldNextjsAppArgs = {
    targetDir: string
    appName?: string
    packageManager?: 'npm' | 'bun'
}

function resolveTargetDir(targetDir: string) {
    const absolutePath = path.resolve(config.repo_root, targetDir)
    if (absolutePath !== config.repo_root && !absolutePath.startsWith(`${config.repo_root}${path.sep}`)) {
        throw new Error('targetDir must stay inside the repository root.')
    }
    return absolutePath
}

async function writeTemplateFile(targetDir: string, relativePath: string, content: string) {
    const filePath = path.join(targetDir, relativePath)
    await mkdir(path.dirname(filePath), { recursive: true })
    await writeFile(filePath, content, 'utf8')
}

function toPackageName(value: string) {
    return value.toLowerCase().replace(/[^a-z0-9-]+/g, '-').replace(/^-|-$/g, '').slice(0, 64) || 'nextjs-app'
}

export default async function scaffoldNextjsApp(args: ScaffoldNextjsAppArgs) {
    const packageManager = args.packageManager || 'npm'
    const appName = args.appName || path.basename(args.targetDir)
    const packageName = toPackageName(appName)
    const absolutePath = resolveTargetDir(args.targetDir.trim())
    const relativePath = path.relative(config.repo_root, absolutePath)

    await mkdir(absolutePath, { recursive: true })

    await writeTemplateFile(absolutePath, 'package.json', JSON.stringify({
        name: packageName,
        version: '0.1.0',
        private: true,
        scripts: {
            dev: 'next dev',
            build: 'next build',
            start: 'next start',
            lint: 'eslint .',
        },
        dependencies: {
            next: '15.5.6',
            react: '19.1.0',
            'react-dom': '19.1.0',
        },
        devDependencies: {
            '@eslint/eslintrc': '^3.3.1',
            '@types/node': '^20.19.25',
            '@types/react': '^19.1.0',
            '@types/react-dom': '^19.1.0',
            eslint: '^9.38.0',
            'eslint-config-next': '^15.5.6',
            typescript: '^5.9.3',
        },
    }, null, 2) + '\n')

    await writeTemplateFile(absolutePath, 'tsconfig.json', JSON.stringify({
        compilerOptions: {
            target: 'ES2017',
            lib: ['dom', 'dom.iterable', 'esnext'],
            allowJs: false,
            skipLibCheck: true,
            strict: true,
            noEmit: true,
            esModuleInterop: true,
            module: 'esnext',
            moduleResolution: 'bundler',
            resolveJsonModule: true,
            isolatedModules: true,
            jsx: 'preserve',
            incremental: true,
            plugins: [{ name: 'next' }],
            paths: { '@/*': ['./src/*'] },
        },
        include: ['next-env.d.ts', '**/*.ts', '**/*.tsx', '.next/types/**/*.ts'],
        exclude: ['node_modules'],
    }, null, 2) + '\n')

    await writeTemplateFile(absolutePath, 'next-env.d.ts', '/// <reference types="next" />\n/// <reference types="next/image-types/global" />\n\n// This file is managed by Next.js.\n')
    await writeTemplateFile(absolutePath, 'next.config.ts', 'import type { NextConfig } from \'next\'\n\nconst nextConfig: NextConfig = {}\n\nexport default nextConfig\n')
    await writeTemplateFile(absolutePath, 'postcss.config.mjs', 'export default {\n  plugins: {},\n}\n')
    await writeTemplateFile(absolutePath, 'eslint.config.mjs', 'import { FlatCompat } from "@eslint/eslintrc"\n\nconst compat = new FlatCompat({ baseDirectory: import.meta.dirname })\n\nexport default [\n  ...compat.extends("next/core-web-vitals", "next/typescript"),\n]\n')
    await writeTemplateFile(absolutePath, 'src/app/layout.tsx', [
        'import "./globals.css"',
        'import type { Metadata } from "next"',
        '',
        'export const metadata: Metadata = {',
        `  title: "${appName}",`,
        '  description: "Generated by Hanasand AI",',
        '}',
        '',
        'export default function RootLayout({ children }: Readonly<{ children: React.ReactNode }>) {',
        '  return (',
        '    <html lang="en">',
        '      <body>{children}</body>',
        '    </html>',
        '  )',
        '}',
        '',
    ].join('\n'))
    await writeTemplateFile(absolutePath, 'src/app/page.tsx', [
        'export default function Home() {',
        '  return (',
        '    <main style={{ minHeight: "100vh", display: "grid", placeItems: "center", padding: "3rem" }}>',
        '      <div>',
        `        <h1>${appName}</h1>`,
        '        <p>Edit <code>src/app/page.tsx</code> to begin.</p>',
        '      </div>',
        '    </main>',
        '  )',
        '}',
        '',
    ].join('\n'))
    await writeTemplateFile(absolutePath, 'src/app/globals.css', [
        ':root {',
        '  color-scheme: light;',
        '}',
        '',
        '* {',
        '  box-sizing: border-box;',
        '}',
        '',
        'html, body {',
        '  margin: 0;',
        '  min-height: 100%;',
        '  font-family: Georgia, "Times New Roman", serif;',
        '  background: #f6f0e8;',
        '  color: #1f1c19;',
        '}',
        '',
        'a {',
        '  color: inherit;',
        '  text-decoration: none;',
        '}',
        '',
    ].join('\n'))

    const installCommand = packageManager === 'bun' ? 'bun install && bun run build' : 'npm install && npm run build'
    const installResult = await runCommand({
        command: installCommand,
        cwd: relativePath,
        timeoutMs: 10 * 60 * 1000,
    })

    return {
        ...installResult,
        absolutePath,
        packageManager,
        appName,
    }
}
