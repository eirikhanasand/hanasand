import { expect, test } from '@playwright/test'
import { readFile } from 'node:fs/promises'
import path from 'node:path'

const root = process.cwd()

test('DWM case detail keeps row-heavy evidence behind disclosures', async () => {
    const source = await readFile(path.join(root, 'src/app/dashboard/dwm/cases/[id]/case-detail-client.tsx'), 'utf8')

    expect(source).toContain('<CollapsiblePanel title=\'Evidence rows\'')
    expect(source).toContain('data-dwm-case-evidence-mobile-list')
    expect(source).toContain('data-dwm-case-evidence-mobile-row')
    expect(source).toContain('data-dwm-case-evidence-desktop-table')
    expect(source).toContain('function EvidenceMobileRow')
    expect(source).toContain('function safeCaseEvidenceExcerpt(row: EvidenceRow)')
    expect(source).toContain('safeEvidenceExcerpt(row.safeExcerpt || row.excerpt')
    expect(source).not.toContain('{row.safeExcerpt || row.excerpt || \'No safe excerpt available.\'}')
    expect(source).toContain('<CollapsiblePanel title=\'Audit timeline\'')
    expect(source).toContain('function CollapsiblePanel')
    expect(source).toContain('CaseCommandBar')
    expect(source).toContain('WorkflowStrip')
    expect(source).toContain('const caseMeta = [')
    expect(source).toContain('compactCaseReference(caseRecord.id, \'Case\')')
    expect(source).toContain('compactCaseReference(scopedOrganizationId, \'Org\')')
    expect(source).toContain('compactCaseReference(scopedAlertId, \'Alert\')')
    expect(source).toContain('compactCaseReference(detail.alert?.id || detail.case?.alertId, \'Alert\')')
    expect(source).toContain('compactCaseReference(organizationId, \'Org\')')
    expect(source).toContain('function compactCaseReference')
    expect(source).not.toContain('{caseRecord.id} · {caseRecord.organizationId || organizationId || \'organization pending\'}')
    expect(source).not.toContain('value={organizationId || tenantId}')
    expect(source).toContain('DecisionBrief')
    expect(source).toContain('data-dwm-case-decision-brief')
    expect(source).toContain('actionDockHref=\'#dwm-case-actions\'')
    expect(source).toContain('id=\'dwm-case-actions\'')
    expect(source).toContain('runAction(action)')
    expect(source).toContain('data-dwm-case-action-blockers')
    expect(source).toContain('function actionUnavailableReasons')
    expect(source).toContain('notifyCustomer')
    expect(source).toContain('sendWebhook')
    expect(source).toContain('data-dwm-webhook-action-blocker')
    expect(source).toContain('Webhook delivery needs a linked alert before it can be tested or sent.')
    expect(source).toContain('function webhookActionBlockedReason')
    expect(source).toContain('detail?.customerNotificationContext?.ready === false')
    expect(source).toContain('Webhook delivery needs an active customer destination before it can be tested or sent.')
    expect(source).toContain('const webhookActionDisabled = Boolean(readOnly || busy !== null || webhookBlockedReason)')
    expect(source).toContain('Manage destination')
    expect(source).toContain('Delivery history')
    expect(source).toContain('focus: \'destinations\'')
    expect(source).toContain('latestDelivery.nextRetryAt')
    expect(source).toContain('latestDelivery.requestId || latestDelivery.auditEventId')
    expect(source).toContain('Case and evidence report')
    expect(source).toContain('data-third-party-report-contract')
    expect(source).toContain('Test STIX report')
    expect(source).toContain('Send STIX report')
    expect(source).toContain('setSelectedEvidenceIds(previous => previous.filter(id => availableIds.includes(id)))')
    expect(source).not.toContain('previous.length ? previous.filter(id => availableIds.includes(id)) : availableIds')
    expect(source).toContain('/api/dwm/webhooks/deliveries')
    expect(source).toContain('deliveries = deliveryRowsFromApi(deliveryPayload)')
    expect(source).toContain('const latestDelivery = deliveries[0]')
    expect(source).not.toContain('state.detail.deliveryContext?.latestDelivery || deliveries[0]')
    expect(source).toContain('Retry exact report')
    expect(source).toContain('latestDelivery?.status === \'failed\' && latestDelivery.retryable === true')
    expect(source).toContain('deliveryId: retryDeliveryId')
    expect(source).toContain('Export JSON report')
    expect(source).toContain('Export STIX 2.1')
    expect(source).not.toContain('What returned')
})
